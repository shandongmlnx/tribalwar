	public static List<CoordXY> getTreePos()
	{
		List<CoordXY> treePosList = new ArrayList<CoordXY>();
		for (int i = 0; i < map.length; i++) {
			for (int j = 0; j < map[i].length; j++) {
				if (map[i][j] == 2)
					treePosList.add(new CoordXY(i, j));
			}
		}
		return treePosList;
	}
